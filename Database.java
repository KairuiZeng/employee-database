    public void saveToText(){
        if (database.getCount() == 0){
            ExportText empty = new ExportText(fileName, false);
            try {
                empty.writeToFile("Empty");
            } catch (IOException ex) {
                Logger.getLogger(Database.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
        boolean firstEmployee = true;
        for (int i=0; i < 10; i++){
                for (int a=0; a < database.getBuckets(i).size(); a++){
                    EmployeeInfo employee = database.getBuckets(i).get(a);
                    
                    try {
                        ExportText erase = new ExportText(fileName, false);
                        ExportText data = new ExportText(fileName, true);
                        if (firstEmployee == true){
                            erase.writeToFile(employee.getFN());
                        } else {
                            data.writeToFile(employee.getFN());
                        }
                       
                	data.writeToFile(employee.getLN());
                        data.writeToFile(String.valueOf(employee.getNumber()));
                	data.writeToFile(String.valueOf(employee.getPlace()));
                	data.writeToFile(String.valueOf(employee.getSex()));
                        data.writeToFile(String.valueOf(employee.getRate()));
                        
                        if (employee instanceof PartTimeEmployee == true){
                            partEmployee = (PartTimeEmployee) database.getBuckets(i).get(a);
                            data.writeToFile("PartTime");
                            data.writeToFile(String.valueOf(partEmployee.getWage()));
                            data.writeToFile(String.valueOf(partEmployee.getHours()));
                            data.writeToFile(String.valueOf(partEmployee.getWeeks()));
                            firstEmployee = false;
                        }
                        else {
                            fullEmployee = (FullTimeEmployee) database.getBuckets(i).get(a);
                            data.writeToFile("FullTime");
                            data.writeToFile(String.valueOf(fullEmployee.getSalary()));
                            firstEmployee = false;
                        }
                    }
                    catch (IOException e) {
			System.out.println(e.getMessage());
                    }
                }
            }
        }
        JOptionPane.showMessageDialog(null, "The database has been saved.");
    }
    
    

    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        
        int num = Integer.parseInt(jTextField7.getText());
        if (database.searchNumber(num)==null){
            JOptionPane.showMessageDialog(null, "This employee has not been found, please try again.");
        } else {
            EmployeeInfo employee = database.searchNumber(num);
            jTextField4.setText(employee.getFN());
            jTextField5.setText(employee.getLN());
            jTextField6.setText(String.valueOf(num));
            jTextField17.setText(String.valueOf(employee.getRate()));
            jComboBox4.setSelectedIndex(employee.getSex());
            jComboBox5.setSelectedIndex(employee.getPlace());
        
            if (employee instanceof PartTimeEmployee == true){
                jPanel4.setVisible(true);
                jPanel8.setVisible(false);
                jPanel7.setVisible(false);
            
                partEmployee = (PartTimeEmployee) employee;
                jTextField18.setText(String.valueOf(partEmployee.getWage()));
                jTextField19.setText(String.valueOf(partEmployee.getHours()));
                jTextField20.setText(String.valueOf(partEmployee.getWeeks()));
            } else {
                jPanel7.setVisible(true);
                jPanel8.setVisible(false);
                jPanel4.setVisible(false);
            
                fullEmployee = (FullTimeEmployee) employee;
                jTextField21.setText(String.valueOf(fullEmployee.getSalary()));
            }
                
        
        
            searchDisplayPanel.setVisible(true);
            addPanel1.setVisible(false);
            welcomePanel.setVisible(false);
            addPanel2.setVisible(false);
            searchPanel.setVisible(false);
            displayPanel.setVisible(false);
        }
        
    }//GEN-LAST:event_jButton8ActionPerformed

    private void jButton18ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton18ActionPerformed
        
        if (jButton18.getText() == "Delete"){
        String[] options = {"Yes", "No"};
            int input = JOptionPane.showOptionDialog(null, "Are you sure you want to delete this employee?",
                "Exit Tab",
                JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE, null, options, options[0]);
            switch (input){
                case 0:
                    int num = Integer.parseInt(jTextField7.getText());
                    EmployeeInfo employee = database.searchNumber(num);
                    database.removeEmployee(employee.getNumber());
                    JOptionPane.showMessageDialog(null, "Employee Deleted");
                    searchDisplayPanel.setVisible(false);
                    searchPanel.setVisible(true);
                    break;
                case 1:
                    JOptionPane.showMessageDialog(null, "Employee Remains In Database.");
                    break;
                default:
            }
        } else {
            String[] options = {"Yes", "No"};
            int input = JOptionPane.showOptionDialog(null, "Change employee type?",
                "Exit Tab",
                JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE, null, options, options[0]);
            switch (input){
                case 0:
                    int num = Integer.parseInt(jTextField7.getText());
                    EmployeeInfo employee = database.searchNumber(num);
                    int type = 0;
                    if (employee instanceof FullTimeEmployee){
                        type = 1;
                    }
                    database.removeEmployee(employee.getNumber());
                    if (type == 0){
                        jPanel4.setVisible(false);
                        jPanel7.setVisible(true);
                    } else {
                        jPanel7.setVisible(false);
                        jPanel4.setVisible(true);
                    }
                    
                    break;
                case 1:
                    JOptionPane.showMessageDialog(null, "Employee Remains the Same.");
                    break;
                default:
        }
        }
    }//GEN-LAST:event_jButton18ActionPerformed


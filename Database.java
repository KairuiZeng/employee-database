    private void jButton15ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton15ActionPerformed
        jTextArea3.setText("");
        
        if (database.getCount() == 0){
            jTextArea3.setText("There are no employees currently in the database.");
        } else {
            jTextArea3.append("The database currently holds " + database.getCount() + " employee(s)." + "\n" + "\n" + '\n');
            for (int i=0; i < 10; i++){
                for (int a=0; a < database.getBuckets(i).size(); a++){
                    EmployeeInfo employee = database.getBuckets(i).get(a);
                    int location = employee.getPlace();
                    String place = employee.getStringPlace(location);
                    
                    if (employee instanceof PartTimeEmployee == true){
                        jTextArea3.append("Part Time Employee " + employee.getNumber() + ": " + employee.getFN() + " " + employee.getLN() + " works in the " + place + " region." + "\n");
                    } else {
                        jTextArea3.append("Full Time Employee " + employee.getNumber() + ": " + employee.getFN() + " " + employee.getLN() + " works in the " + place + " region." + "\n");
                    }
                }
            }
        }
    }//GEN-LAST:event_jButton15ActionPerformed


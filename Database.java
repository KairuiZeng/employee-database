    private void jButton22ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton22ActionPerformed
        // TODO add your handling code here:
        try{
            ImportText file = new ImportText(fileName);
            String[] lineStore = file.OpenFile();
            int i = 0;
            if (lineStore[i] == "Empty") {
                JOptionPane.showMessageDialog(null, "There were no entries saved previously.");
            } 
            else {
            while (lineStore[i] != null){
            first = (lineStore[i]); i++;
            last = (lineStore[i]); i++;
            empNum = Integer.parseInt((lineStore[i])); i++;
            workplace = Integer.parseInt((lineStore[i])); i++;
            gender = Integer.parseInt((lineStore[i])); i++;
            rate = Double.parseDouble((lineStore[i])); i++;
            
            if (lineStore[i] == "PartTime"){
                i++;
                wage = Double.parseDouble((lineStore[i])); i++;
                week = Double.parseDouble((lineStore[i])); i++;
                year = Double.parseDouble((lineStore[i])); i++;
                
                if (database.searchNumber(empNum)==null){
                    partEmployee = new PartTimeEmployee(empNum, first, last, gender, workplace, rate, wage, week, year);
                    database.addEmployee(partEmployee);
                }
            
            } else {
                i++;
                salary = Double.parseDouble((lineStore[i])); i++;
                if (database.searchNumber(empNum) == null){
                fullEmployee = new FullTimeEmployee(empNum, first, last, gender, workplace, rate, salary);
                database.addEmployee(fullEmployee);
                }
            } 
            }
            }   
        } 
        catch (IOException e) {
            System.out.println(e.getMessage());
            System.out.println("File could not be found! Oh No!");
        }
    }//GEN-LAST:event_jButton22ActionPerformed
    
